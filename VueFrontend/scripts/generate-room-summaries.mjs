import { readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'
import { toRoomSummaryRecord } from '../src/features/rooms/model/room-record.js'
import { loadRoomSourceCatalog } from './lib/load-room-content.mjs'

const scriptPath = fileURLToPath(import.meta.url)
const rootDir = resolve(dirname(scriptPath), '..')

export const roomSummaryOutputPath = resolve(
  rootDir,
  'src/features/rooms/content/summary-data.generated.js',
)

export function renderRoomSummaryModule(rooms) {
  const summaries = rooms.map(toRoomSummaryRecord)

  return [
    '// Generated by scripts/generate-room-summaries.mjs from canonical room modules.',
    '// Do not edit this file directly.',
    `export const roomSummarySourceCatalog = ${JSON.stringify(summaries, null, 2)}`,
    '',
  ].join('\n')
}

async function readCurrentOutput() {
  try {
    return await readFile(roomSummaryOutputPath, 'utf8')
  }
  catch (error) {
    if (error.code === 'ENOENT') {
      return null
    }

    throw error
  }
}

async function generateRoomSummaries() {
  const rooms = await loadRoomSourceCatalog()
  const output = renderRoomSummaryModule(rooms)
  const currentOutput = await readCurrentOutput()

  if (currentOutput === output) {
    console.log(`room summaries current (${rooms.length} rooms)`)
    return
  }

  await writeFile(roomSummaryOutputPath, output)
  console.log(`room summaries generated (${rooms.length} rooms)`)
}

if (resolve(process.argv[1] || '') === scriptPath) {
  await generateRoomSummaries()
}
